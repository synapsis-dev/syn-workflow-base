
export interface IDialogDefinition {
    /**
     * Page ids for this dialog.
     *
     * @readonly
     * @type {unknown}
     * @memberof IDialogDefinition
     */
    Pages: unknown;

    /**
     * Section ids for this dialog.
     *
     * @readonly
     * @type {unknown}
     * @memberof IDialogDefinition
     */
    Sections: unknown;

    /**
     * Row ids for this dialog.
     *
     * @readonly
     * @type {unknown}
     * @memberof IDialogDefinition
     */
    Rows: unknown;

    /**
     * Column ids for this dialog.
     *
     * @readonly
     * @type {unknown}
     * @memberof IDialogDefinition
     */
    Columns: unknown;

    /**
     * Element definitions for this dialog.
     *
     * @readonly
     * @type {unknown}
     * @memberof IDialogDefinition
     */
    Elements: unknown;
}
